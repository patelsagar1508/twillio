            <h2 style="margin-top: 8px;margin-bottom: 8px; padding: 5px;">Vinyl Care &amp; Cleaning Guide</h2>
            <p><i>When using vinyl cushions most event debris can be easily wiped away with a damp cloth. But for cushions that require extra cleaning please see below.</i></p>
            <div class="spacer_height10"></div>
            <p><b>Stain Type A:</b> grease, black felt tip pen, chocolate, eye shadow, mascara, suntan lotion </p>
            <p><b>Stain Type B:</b> red lipstick</p>
            <p><b>Stain Type C:</b> blood, tar, asphalt, oil based paint, urine, feces</p>
            <p><b>Stain Type D:</b> other Tough Stains</p>
            <div class="spacer_height10"></div>
        </div>
        <div style="clear: both;"></div>
        <p><b>For A Stains:</b> Remove excess spill with a damp cloth. Clean with a 1:1 mix of a mild liquid soap and water. Rinse with clean water and dry.</p>
        <p><b>For B Stains:</b> Use a straight application of concentrated cleansers such as Formula 409 or Fantastik spray cleaner. Wipe with a clean cloth, rinse with water and dry.</p>
        <p><b>For C Stains:</b> Apply a 1:1 mix of ammonia and water or a 1:4 mix of bleach and water. Rinse with clean water and dry.</p>
        <p><b>For D Stains:</b> Use a straight application of naphtha (lighter fluid). Rinse thoroughly with clean water and pat surface dry.</p>
        <p><b>For D Stains:</b> Use a 1:1 mix of isopropyl alcohol and water. If the stain persists, use straight alcohol. Rinse thoroughly with clean water and pat dry.</p>
        <div class="spacer_height10"></div>
        <p><i><b>Note:</b> For cleaning use a soft cotton cloth saturated with the cleaning material and rub the stain in light circles. Pat dry with another soft cotton cloth and check the results.</i></p>
